package agregatorevents;

public class Event {
	
	private String priority;
	private String statusEvent;
	private String nameEvent;
	private String valueEvent;
	
	
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	public String getStatusEvent() {
		return statusEvent;
	}
	public void setStatusEvent(String statusEvent) {
		this.statusEvent = statusEvent;
	}
	public String getNameEvent() {
		return nameEvent;
	}
	public void setNameEvent(String nameEvent) {
		this.nameEvent = nameEvent;
	}
	public String getValueEvent() {
		return valueEvent;
	}
	public void setValueEvent(String valueEvent) {
		this.valueEvent = valueEvent;
	}
	
	
	

}
